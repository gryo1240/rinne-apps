// 存在しない占星術(Web版) 生成ロジック
// 生年月日(+任意の名前)から、同じ入力なら常に同じ「架空の星座」を返す(決定論的)。
"use strict";

const SeizaGenerator = (() => {
  const enc = new TextEncoder();

  function fnv(str) {
    let h = 2166136261 >>> 0;
    const bytes = enc.encode(str);
    for (let i = 0; i < bytes.length; i++) {
      h = (h ^ bytes[i]) >>> 0;
      h = Math.imul(h, 16777619) >>> 0;
    }
    return h >>> 0;
  }
  function pick(arr, h) { return arr[h % arr.length]; }

  const PREFIX = [
    "静寂の", "追憶の", "黄昏の", "明星の", "夢見る", "刻の", "遠雷の", "花霞の",
    "蒼穹の", "未明の", "残響の", "朝凪の", "雨上がりの", "灯火の", "迷い", "眠れる",
    "風待ちの", "水面の", "旅路の", "無音の",
  ];
  const CORE = [
    "兎", "鈴", "硝子", "羅針盤", "迷宮", "天秤", "蝶", "泉", "階段", "扉",
    "灯台", "波", "種", "鍵", "螺旋", "橋", "帆", "杯", "砂", "羽根",
  ];
  const HUE = [
    "銀", "蒼", "緋", "白金", "深紅", "翡翠", "琥珀", "群青", "黎明", "漆黒",
    "朱", "藍", "象牙", "若草", "茜", "瑠璃", "桜", "菫", "橙", "灰白",
  ];
  const BODY = [
    "彗星", "環", "月", "星雲", "双子星", "灯", "波動", "光珠", "砂時計", "泉",
    "羅針", "小惑星", "極光", "残光", "潮騒", "眠り星",
  ];
  const TEMPER = [
    "物事の奥にある、小さな真実に気づきやすい人です。",
    "静かな場所でこそ、本当の力を発揮するタイプです。",
    "人の話をじっくり聞ける、頼れる相談役になれる星回りです。",
    "思いついたら即行動、周りを驚かせる瞬発力の持ち主です。",
    "一度決めたことは最後までやり遂げる、芯の強さがあります。",
    "新しいものへの好奇心が、あなたを遠くまで連れていきます。",
    "誰かの背中をそっと押す、縁の下の力持ちの星です。",
    "感情の機微に敏感で、物語や音楽に心を動かされやすい人です。",
    "マイペースに見えて、実はしっかり周りを見ている星です。",
    "困ったときほど冷静になれる、頼もしさを秘めています。",
    "小さな幸せを見つけるのが上手な、日常を彩る星回りです。",
    "人と人をつなぐ、橋渡し役になりやすい星です。",
    "直感を信じて動くと、良い流れに乗りやすいタイプです。",
    "コツコツ積み上げた努力が、静かに実を結ぶ星です。",
    "笑顔でその場の空気を和ませる、天性の癒やし役です。",
    "一人の時間を大切にすることで、力が満ちていく星です。",
    "変化を恐れず、むしろ楽しんでしまう身軽さがあります。",
    "誠実さが、いつか大きな信頼となって返ってくる星です。",
  ];
  const COLORS = [
    "深藍", "萌黄", "紅緋", "薄浅葱", "山吹", "桔梗", "灰桜", "若竹",
    "琥珀", "白群", "朱華", "青磁", "菖蒲", "生成り", "檜皮", "瑠璃紺",
  ];
  const ITEMS = [
    "小さな鈴", "古い鍵", "折りたたみの傘", "手のひらサイズの鏡", "一輪の花",
    "しおり", "貝殻", "万年筆", "丸い石", "小瓶", "羽根ペン", "砂時計",
    "ハンカチ代わりの手ぬぐい", "星の描かれたカード", "ガラスの玉", "小さな鈴虫の音",
  ];
  const TIMES = [
    "暁の刻", "宵の口", "黄昏時", "昼下がりの刻", "夜半", "朝凪の刻",
    "夕凪の刻", "星瞬く刻", "月渡る刻", "風待ちの刻", "未明", "薄明の刻",
  ];
  const DIRECTIONS = [
    "月の昇る方", "星の流れる方", "風上", "陽の沈む方", "霧の立つ方",
    "潮の満ちる方", "雲の切れる方", "灯りのともる方", "北の窓の方", "旅立ちの方",
  ];
  const TODAY_MSG = [
    "今日はいつもより少し、空を見上げてみてくださいね。",
    "小さな用事を一つ片付けると、心がすっと軽くなりそうです。",
    "誰かに「ありがとう」を伝えると、良い一日になりそうですよ。",
    "急がず、いつもの半分くらいの速さで過ごしてみてください。",
    "気になっていたことを、そっと片付けるのに向いている日です。",
    "温かい飲み物を一杯、ゆっくり味わう時間を作ってみて。",
    "新しいことより、いつもの習慣を丁寧に。それだけで十分です。",
    "誰かの言葉に、思いがけないヒントが隠れているかもしれません。",
    "少し早めに休むと、明日がもっと軽やかになりますよ。",
    "窓を開けて、深呼吸を一つ。それだけで整う日です。",
    "後回しにしていた連絡、今日がちょうど良いタイミングかもしれません。",
    "無理に元気を出さなくて大丈夫。静かに過ごすのも星回りのうちです。",
  ];

  function seedBase(y, m, d, name) {
    const nm = (name || "").trim();
    return `${y}-${m}-${d}|${nm}`;
  }

  // 生年月日(+名前)から、常に同じ「あなただけの架空星座」を生成する
  function generate(y, m, d, name) {
    const base = seedBase(y, m, d, name);
    const starName = pick(PREFIX, fnv(base + "|p")) + pick(CORE, fnv(base + "|c")) + "座";
    const guardian = pick(HUE, fnv(base + "|h")) + "の" + pick(BODY, fnv(base + "|b"));
    const temper = pick(TEMPER, fnv(base + "|t"));
    const color = pick(COLORS, fnv(base + "|lc"));
    const item = pick(ITEMS, fnv(base + "|li"));
    const luckyTime = pick(TIMES, fnv(base + "|lt"));
    const luckyDirection = pick(DIRECTIONS, fnv(base + "|ld"));

    // 星図描画用: 5〜8個の星を、種から決定論的に配置する
    const starCount = 5 + (fnv(base + "|n") % 4); // 5-8
    const stars = [];
    for (let i = 0; i < starCount; i++) {
      const hx = fnv(base + "|sx" + i);
      const hy = fnv(base + "|sy" + i);
      const hr = fnv(base + "|sr" + i);
      stars.push({
        x: 0.12 + (hx % 1000) / 1000 * 0.76,
        y: 0.14 + (hy % 1000) / 1000 * 0.62,
        r: 2.2 + (hr % 100) / 100 * 2.4,
      });
    }

    return { starName, guardian, temper, color, item, luckyTime, luckyDirection, stars, base };
  }

  // 「今日のひとこと」は日付だけで決まる(星座に関係なく毎日変わる)
  function todayMessage(dateStr) {
    return pick(TODAY_MSG, fnv("today|" + dateStr));
  }

  return { generate, todayMessage, fnv };
})();
